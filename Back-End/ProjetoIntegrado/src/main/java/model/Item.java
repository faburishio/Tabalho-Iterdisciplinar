package model;

public class Item {
    private int iditem;
    private String nomeItem;
    private String descItem;
    private double valItem;
    private String catItem;


    public int getIditem() {
        return this.iditem;
    }

    public void setIditem(int iditem) {
        this.iditem = iditem;
    }

    public String getNomeItem() {
        return this.nomeItem;
    }

    public void setNomeItem(String nomeItem) {
        this.nomeItem = nomeItem;
    }

    public String getDescItem() {
        return this.descItem;
    }

    public void setDescItem(String descItem) {
        this.descItem = descItem;
    }

    public double getValItem() {
        return this.valItem;
    }

    public void setValItem(double valItem) {
        this.valItem = valItem;
    }

    public String getCatItem() {
        return this.catItem;
    }

    public void setCatItem(String catItem) {
        this.catItem = catItem;
    }

    
}